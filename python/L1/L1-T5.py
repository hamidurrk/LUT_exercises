num1 = int(input("Give the first integer:\n"))
num2 = int(input("Give the second integer:\n"))

print(f"{num1} + {num2} = {num1+num2}")
print(f"{num1} - {num2} = {num1-num2}")
print(f"{num1} * {num2} = {num1*num2}")
print(f"{num1} / {num2} = {num1/num2}")
print(f"{num1} // {num2} = {num1//num2}")
print(f"{num1} ** {num2} = {num1**num2}")
print(f"{num1} % {num2} = {num1%num2}")